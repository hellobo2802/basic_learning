# 基本运算符
'''
1. 算术运算符 + - * / % **
2. 比较运算符 > < == <= >= != 

'''

# 1 算术运算符 
print(5 + 5.5) # 转为浮点型
print('我是' + '刘少博') # 不推荐，效率太低！！
print([1,2,3] + [4,5,6]) # 列表的合并拼接
print(5 - 2)
print(10 * 2)
print(10 / 3) # 有小数保留
print(10 // 3) # 地板除 只保留整数部分，不会四舍五入！！
print(10 % 3) # 取模，取余数运算
print(2 ** 3) # 2的3次方

# 2 比较运算符
print(20 > 18) # T
print(20 < 18) # F
print(20 == 18) # F
print(20 >= 18) # T
print(20 <= 18) # F
print(20 != 18) # T

